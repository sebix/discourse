CREATE TABLE posts
(
    id                      TEXT     NOT NULL PRIMARY KEY,
    action_code             TEXT,
    baked_at                DATETIME,
    baked_version           INTEGER,
    bookmark_count          INTEGER,
    cook_method             INTEGER,
    cooked                  TEXT     NOT NULL,
    created_at              DATETIME NOT NULL,
    deleted_at              DATETIME,
    deleted_by_id           TEXT,
    edit_reason             TEXT,
    hidden                  BOOLEAN,
    hidden_at               DATETIME,
    hidden_reason_id        TEXT,
    illegal_count           INTEGER,
    image_upload_id         TEXT,
    inappropriate_count     INTEGER,
    incoming_link_count     INTEGER,
    last_editor_id          TEXT,
    last_version_at         DATETIME NOT NULL,
    like_count              INTEGER,
    like_score              INTEGER,
    locked_by_id            TEXT,
    notify_moderators_count INTEGER,
    notify_user_count       INTEGER,
    off_topic_count         INTEGER,
    outbound_message_id     TEXT,
    percent_rank            FLOAT,
    post_number             INTEGER  NOT NULL,
    post_type               INTEGER,
    public_version          INTEGER,
    quote_count             INTEGER,
    raw                     TEXT     NOT NULL,
    raw_email               TEXT,
    reads                   INTEGER,
    reply_count             INTEGER,
    reply_quoted            BOOLEAN,
    reply_to_post_number    INTEGER,
    reply_to_user_id        TEXT,
    score                   FLOAT,
    self_edits              INTEGER,
    sort_order              INTEGER,
    spam_count              INTEGER,
    topic_id                TEXT     NOT NULL,
    user_deleted            BOOLEAN,
    user_id                 TEXT,
    version                 INTEGER,
    via_email               BOOLEAN,
    wiki                    BOOLEAN,
    word_count              INTEGER
);

CREATE TABLE users
(
    id                        TEXT     NOT NULL PRIMARY KEY,
    active                    BOOLEAN,
    admin                     BOOLEAN,
    approved                  BOOLEAN,
    approved_at               DATETIME,
    approved_by_id            TEXT,
    created_at                DATETIME NOT NULL,
    date_of_birth             DATE,
    first_seen_at             DATETIME,
    flair_group_id            TEXT,
    group_locked_trust_level  INTEGER,
    ip_address                TEXT,
    last_seen_at              DATETIME,
    locale                    TEXT,
    manual_locked_trust_level INTEGER,
    moderator                 BOOLEAN,
    name                      TEXT,
    previous_visit_at         DATETIME,
    primary_group_id          TEXT,
    registration_ip_address   TEXT,
    required_fields_version   INTEGER,
    silenced_till             DATETIME,
    staged                    BOOLEAN,
    suspended_at              DATETIME,
    suspended_till            DATETIME,
    title                     TEXT,
    trust_level               INTEGER  NOT NULL,
    uploaded_avatar_id        TEXT,
    username                  TEXT     NOT NULL,
    views                     INTEGER
);

